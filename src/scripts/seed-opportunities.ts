// ── Insert into Supabase ──────────────────────────────────────────────────────

async function seed() {
  console.log(`Seeding ${opportunities.length} opportunities…`)

  // deadline column is type `date` in DB — strip the human-readable string.
  // deadline_date stays as ISO text; rolling entries get null so daysUntil() returns null.
  const rows = opportunities.map(({ deadline: _drop, deadline_date, ...rest }) => ({
    ...rest,
    deadline_date: deadline_date === 'Rolling basis' ? null : deadline_date,
  }))

  const { data, error } = await supabase
    .from('opportunities')
    .insert(rows)
    .select('id, title')

  if (error) {
    console.error('Insert error:', error)
    process.exit(1)
  }

  console.log(`\n✓ Inserted ${data?.length ?? 0} rows:\n`)
  data?.forEach((row: { id: string; title: string }) => console.log(`  ${row.id}  ${row.title}`))
  console.log('\nDone.')
}

